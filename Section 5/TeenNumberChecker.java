public class TeenNumberChecker {




    public static boolean hasTeen (int x, int y, int z){


        return ( (x >= 13 && x <= 19) ||
                (y >= 13 && y <= 19)  ||
            (z >= 13 && z <= 19)
                );




    }


    public static boolean isTeen (int x){

      return   x >= 13 && x <= 19 ;

    }





}
